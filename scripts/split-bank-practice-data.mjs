import { mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { readBankBackedPracticeSets } from "./practice-data-utils.mjs";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const generatedDir = path.join(root, "src/lib/generated");
const setsDir = path.join(generatedDir, "bank-practice-sets");
const indexPath = path.join(generatedDir, "bankPracticeIndex.generated.ts");

const HEADER = "// AUTO-GENERATED by scripts/split-bank-practice-data.mjs — do not edit.";

const FREE_RESPONSE_FLAG = 1;
const UNOFFICIAL_BANK_FLAG = 2;
const REPLACEMENT_FLAG = 4;

const readMonolithSection = (text, marker, terminator) => {
  const start = text.indexOf(marker);
  if (start === -1) throw new Error(`Could not find "${marker}"`);
  const end = text.indexOf(terminator, start);
  if (end === -1) throw new Error(`Could not find terminator for "${marker}"`);
  return text.slice(start, end + terminator.length);
};

const monolithText = readFileSync(path.join(generatedDir, "bankPractice.generated.ts"), "utf8");
const summaryStart = monolithText.indexOf("export const BANK_PRACTICE_SUMMARY = ");
if (summaryStart === -1) throw new Error("Could not find BANK_PRACTICE_SUMMARY");
const typeBlock = monolithText.slice(0, summaryStart).trim();
const summaryBlock = readMonolithSection(monolithText, "export const BANK_PRACTICE_SUMMARY = ", "} as const;");

const practiceSets = readBankBackedPracticeSets(root);

const encodeQuestion = (question) => {
  const bankQuestion = question.bankQuestion;
  const flags =
    (bankQuestion.type === "free-response" ? FREE_RESPONSE_FLAG : 0) |
    (bankQuestion.bankType === "unofficial" ? UNOFFICIAL_BANK_FLAG : 0) |
    (question.isReplacement ? REPLACEMENT_FLAG : 0);
  return [bankQuestion.sourceId, bankQuestion.id, flags];
};

const encodedSets = practiceSets.map((practiceSet) => ({
  id: practiceSet.id,
  setNumber: practiceSet.setNumber,
  modules: practiceSet.modules.map(({ questions, ...module }) => ({
    ...module,
    encodedQuestions: questions.map(encodeQuestion),
  })),
}));

const decodeQuestionRef = (module, [sourceId, questionId, flags], index) => {
  const bankType = flags & UNOFFICIAL_BANK_FLAG ? "unofficial" : "past";
  return {
    slot: index + 1,
    bankQuestion: {
      id: questionId,
      stableId: `bank-${bankType}-${module.subject}-${sourceId}`,
      bankType,
      subject: module.subject,
      sourceId,
      type: flags & FREE_RESPONSE_FLAG ? "free-response" : "multiple-choice",
    },
    isReplacement: (flags & REPLACEMENT_FLAG) !== 0,
  };
};

for (const [setIndex, practiceSet] of practiceSets.entries()) {
  const encodedSet = encodedSets[setIndex];
  for (const [moduleIndex, module] of practiceSet.modules.entries()) {
    const encodedModule = encodedSet.modules[moduleIndex];
    for (const [questionIndex, question] of module.questions.entries()) {
      const expected = JSON.stringify({
        slot: question.slot,
        bankQuestion: {
          id: question.bankQuestion.id,
          stableId: question.bankQuestion.stableId,
          bankType: question.bankQuestion.bankType,
          subject: question.bankQuestion.subject,
          sourceId: question.bankQuestion.sourceId,
          type: question.bankQuestion.type,
        },
        isReplacement: question.isReplacement,
      });
      const decoded = JSON.stringify(
        decodeQuestionRef(encodedModule, encodedModule.encodedQuestions[questionIndex], questionIndex),
      );
      if (decoded !== expected) {
        throw new Error(`Encoded question mismatch in ${module.slug} slot ${question.slot}`);
      }
    }
  }
}

const indexSource = `${HEADER}

${typeBlock}

export type GeneratedPracticeQuestionRef = {
  slot: number;
  bankQuestion: Pick<BankQuestion, "id" | "stableId" | "bankType" | "subject" | "sourceId" | "type">;
  isReplacement: boolean;
};

export type GeneratedPracticeModuleRef = Omit<GeneratedPracticeModule, "questions"> & {
  questions: GeneratedPracticeQuestionRef[];
};

export type GeneratedPracticeSetRef = {
  id: string;
  setNumber: number;
  modules: GeneratedPracticeModuleRef[];
};

${summaryBlock}

type EncodedPracticeQuestion = readonly [sourceId: string, questionId: number, flags: number];

type EncodedPracticeModule = Omit<GeneratedPracticeModuleRef, "questions"> & {
  encodedQuestions: readonly EncodedPracticeQuestion[];
};

type EncodedPracticeSet = {
  id: string;
  setNumber: number;
  modules: EncodedPracticeModule[];
};

const FREE_RESPONSE_FLAG = ${FREE_RESPONSE_FLAG};
const UNOFFICIAL_BANK_FLAG = ${UNOFFICIAL_BANK_FLAG};
const REPLACEMENT_FLAG = ${REPLACEMENT_FLAG};

const ENCODED_PRACTICE_SETS: EncodedPracticeSet[] = [
${encodedSets.map((encodedSet) => `  ${JSON.stringify(encodedSet)},`).join("\n")}
];

export const BANK_PRACTICE_SET_REFS: GeneratedPracticeSetRef[] = ENCODED_PRACTICE_SETS.map((encodedSet) => ({
  id: encodedSet.id,
  setNumber: encodedSet.setNumber,
  modules: encodedSet.modules.map(({ encodedQuestions, ...module }) => ({
    ...module,
    questions: encodedQuestions.map(([sourceId, questionId, flags], index) => {
      const bankType = flags & UNOFFICIAL_BANK_FLAG ? ("unofficial" as const) : ("past" as const);
      return {
        slot: index + 1,
        bankQuestion: {
          id: questionId,
          stableId: \`bank-\${bankType}-\${module.subject}-\${sourceId}\`,
          bankType,
          subject: module.subject,
          sourceId,
          type: flags & FREE_RESPONSE_FLAG ? ("free-response" as const) : ("multiple-choice" as const),
        },
        isReplacement: (flags & REPLACEMENT_FLAG) !== 0,
      };
    }),
  })),
}));

export const GENERATED_PRACTICE_SET_LOADERS: Record<
  number,
  () => Promise<{ GENERATED_PRACTICE_SET: GeneratedPracticeSet }>
> = {
${practiceSets.map((practiceSet) => `  ${practiceSet.setNumber}: () => import("./bank-practice-sets/set-${practiceSet.setNumber}.generated"),`).join("\n")}
};
`;

rmSync(setsDir, { recursive: true, force: true });
mkdirSync(setsDir, { recursive: true });

for (const practiceSet of practiceSets) {
  const setSource = `${HEADER}

import type { GeneratedPracticeSet } from "../bankPracticeIndex.generated";

export const GENERATED_PRACTICE_SET: GeneratedPracticeSet = ${JSON.stringify(practiceSet, null, 2)};
`;
  writeFileSync(path.join(setsDir, `set-${practiceSet.setNumber}.generated.ts`), setSource);
}

writeFileSync(indexPath, indexSource);

console.log(`Wrote ${practiceSets.length} practice set files and bankPracticeIndex.generated.ts`);
