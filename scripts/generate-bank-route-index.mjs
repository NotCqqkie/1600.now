#!/usr/bin/env node

import { mkdirSync, rmSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createServer } from "vite";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

const sources = ["past", "unofficial", "all"];
const detailSources = ["past", "unofficial"];
const subjects = ["math", "reading"];
const DETAIL_SHARD_SIZE = 64;

const server = await createServer({
  appType: "custom",
  logLevel: "error",
  server: { middlewareMode: true },
});

try {
  const { loadBankPool } = await server.ssrLoadModule("/src/data/questionBank.ts");
  const generatedDir = path.join(root, "src/lib/generated/bank-route-index");
  const detailShardDir = path.join(root, "public/generated/bank-question-shards");
  const legacyDetailShardDir = path.join(root, "src/lib/generated/bank-question-shards");
  let totalRouteRows = 0;
  let totalDetailRows = 0;
  rmSync(generatedDir, { recursive: true, force: true });
  rmSync(detailShardDir, { recursive: true, force: true });
  rmSync(legacyDetailShardDir, { recursive: true, force: true });
  mkdirSync(generatedDir, { recursive: true });
  mkdirSync(detailShardDir, { recursive: true });

  const detailShardByQuestion = new Map();

  for (const source of detailSources) {
    for (const subject of subjects) {
      const questions = await loadBankPool(subject, source);
      totalDetailRows += questions.length;

      for (let shardIndex = 0; shardIndex * DETAIL_SHARD_SIZE < questions.length; shardIndex += 1) {
        const shardQuestions = questions.slice(
          shardIndex * DETAIL_SHARD_SIZE,
          (shardIndex + 1) * DETAIL_SHARD_SIZE,
        );
        for (const question of shardQuestions) {
          detailShardByQuestion.set(
            `${question.bankType}:${subject}:${question.sourceId}`,
            shardIndex,
          );
        }

        writeFileSync(
          path.join(detailShardDir, `${source}-${subject}-${shardIndex}.json`),
          JSON.stringify(shardQuestions),
        );
      }
    }
  }

  for (const source of sources) {
    for (const subject of subjects) {
      const questions = await loadBankPool(subject, source);
      const rows = questions.map((question) => [
        question.id,
        question.stableId,
        question.sourceId,
        question.bankType,
        question.difficulty ?? null,
        detailShardByQuestion.get(`${question.bankType}:${subject}:${question.sourceId}`) ?? 0,
      ]);
      totalRouteRows += rows.length;
      const header = `// AUTO-GENERATED by scripts/generate-bank-route-index.mjs - do not edit.

export type BankRouteIndexRow = readonly [
  id: number,
  stableId: string,
  sourceId: string,
  bankType: "past" | "unofficial",
  difficulty: "Easy" | "Medium" | "Hard" | null,
  detailShard: number,
];

`;

      writeFileSync(
        path.join(generatedDir, `${source}-${subject}.generated.ts`),
        `${header}export const BANK_ROUTE_INDEX_ROWS: readonly BankRouteIndexRow[] = ${JSON.stringify(rows)};\n`,
      );
    }
  }

  rmSync(path.join(root, "src/lib/generated/bankRouteIndex.generated.ts"), { force: true });

  console.log(
    `generate-bank-route-index: wrote ${totalRouteRows} route rows and ${totalDetailRows} detail rows`,
  );
} finally {
  await server.close();
}
