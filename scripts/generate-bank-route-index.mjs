#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { createHash } from "node:crypto";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createServer } from "vite";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

// questionBank.ts loads its raw question JSON via fetch of ?url asset imports;
// in this Node/SSR context those resolve to root-relative dev URLs, so map
// them onto the filesystem.
const nativeFetch = globalThis.fetch;
globalThis.fetch = async (input, init) => {
  const url = typeof input === "string" ? input : input.url;
  if (url.startsWith("/")) {
    const body = readFileSync(path.join(root, url.split("?")[0]));
    return new Response(body, { status: 200, headers: { "Content-Type": "application/json" } });
  }
  return nativeFetch(input, init);
};

const sources = ["past", "unofficial", "all"];
const detailSources = ["past", "unofficial"];
const subjects = ["math", "reading"];
const DETAIL_SHARD_SIZE = 64;

const normalizeQuestionSearchText = (value) =>
  (value ?? "")
    .toLowerCase()
    .replace(/<[^>]*>/g, " ")
    .replace(/\s+/g, " ")
    .trim();

const getQuestionSearchText = (question) =>
  [
    question.prompt,
    question.passage,
    question.questionText,
    question.choices?.map((choice) => choice.text || "").join(" "),
    question.correctAnswer,
  ]
    .map((value) => normalizeQuestionSearchText(value))
    .join(" ");

const getQuestionPreviewText = (question) =>
  (question.questionText || question.prompt || question.passage || "Question preview unavailable")
    .replace(/<[^>]*>/g, " ")
    .replace(/\s+/g, " ")
    .trim();

// questionBank.ts statically imports bankDataVersion.generated.ts, which this
// script itself emits — bootstrap a placeholder on fresh checkouts so
// ssrLoadModule can resolve it.
const bankDataVersionPath = path.join(root, "src/lib/generated/bankDataVersion.generated.ts");
if (!existsSync(bankDataVersionPath)) {
  writeFileSync(
    bankDataVersionPath,
    `// AUTO-GENERATED by scripts/generate-bank-route-index.mjs - do not edit.

export const BANK_DATA_VERSION = "000000000000";
`,
  );
}

const server = await createServer({
  appType: "custom",
  logLevel: "error",
  server: { middlewareMode: true },
});

try {
  const { loadBankPool, loadAllSourceBankQuestions } = await server.ssrLoadModule("/src/data/questionBank.ts");
  const generatedDir = path.join(root, "src/lib/generated/bank-route-index");
  const detailShardDir = path.join(root, "public/generated/bank-question-shards");
  const searchIndexDir = path.join(root, "public/generated/bank-search");
  const legacyDetailShardDir = path.join(root, "src/lib/generated/bank-question-shards");
  let totalRouteRows = 0;
  let totalDetailRows = 0;
  let totalSearchRows = 0;
  rmSync(generatedDir, { recursive: true, force: true });
  rmSync(detailShardDir, { recursive: true, force: true });
  rmSync(searchIndexDir, { recursive: true, force: true });
  rmSync(legacyDetailShardDir, { recursive: true, force: true });
  mkdirSync(generatedDir, { recursive: true });
  mkdirSync(detailShardDir, { recursive: true });
  mkdirSync(searchIndexDir, { recursive: true });

  const detailShardByQuestion = new Map();
  // Content hash over every runtime-fetched JSON file; baked into the app as
  // a ?v= cache-buster so these files can be served with immutable caching.
  const runtimeDataHash = createHash("sha256");

  for (const source of detailSources) {
    for (const subject of subjects) {
      const questions =
        subject === "reading"
          ? await loadAllSourceBankQuestions(subject, source)
          : await loadBankPool(subject, source);
      totalDetailRows += questions.length;

      for (let shardIndex = 0; shardIndex * DETAIL_SHARD_SIZE < questions.length; shardIndex += 1) {
        const shardQuestions = questions.slice(
          shardIndex * DETAIL_SHARD_SIZE,
          (shardIndex + 1) * DETAIL_SHARD_SIZE,
        );
        for (const question of shardQuestions) {
          detailShardByQuestion.set(
            `${question.bankType}:${subject}:${question.sourceId}`,
            shardIndex,
          );
        }

        const shardJson = JSON.stringify(shardQuestions);
        runtimeDataHash.update(shardJson);
        writeFileSync(
          path.join(detailShardDir, `${source}-${subject}-${shardIndex}.json`),
          shardJson,
        );
      }
    }
  }

  for (const source of sources) {
    const searchRows = [];

    for (const subject of subjects) {
      const visibleQuestions = await loadBankPool(subject, source);
      searchRows.push(
        ...visibleQuestions.map((question) => [
          question.id,
          question.stableId,
          question.sourceId,
          question.bankType,
          question.subject,
          question.difficulty ?? null,
          question.category.domain,
          question.category.skill,
          question.inPracticeTests === true,
          getQuestionPreviewText(question),
          getQuestionSearchText(question),
        ]),
      );

      let questions = visibleQuestions;
      if (subject === "reading") {
        const visibleQuestionKeys = new Set(
          visibleQuestions.map((question) => `${question.bankType}:${question.sourceId}`),
        );
        questions = [
          ...visibleQuestions,
          ...(await loadAllSourceBankQuestions(subject, source)).filter(
            (question) => !visibleQuestionKeys.has(`${question.bankType}:${question.sourceId}`),
          ),
        ];
      }
      const rows = questions.map((question, index) => [
        index + 1,
        question.stableId,
        question.sourceId,
        question.bankType,
        question.difficulty ?? null,
        detailShardByQuestion.get(`${question.bankType}:${subject}:${question.sourceId}`) ?? 0,
      ]);
      totalRouteRows += rows.length;
      const header = `// AUTO-GENERATED by scripts/generate-bank-route-index.mjs - do not edit.

export type BankRouteIndexRow = readonly [
  id: number,
  stableId: string,
  sourceId: string,
  bankType: "past" | "unofficial",
  difficulty: "Easy" | "Medium" | "Hard" | null,
  detailShard: number,
];

`;

      writeFileSync(
        path.join(generatedDir, `${source}-${subject}.generated.ts`),
        `${header}export const BANK_ROUTE_INDEX_ROWS: readonly BankRouteIndexRow[] = ${JSON.stringify(rows)};\n`,
      );
    }

    totalSearchRows += searchRows.length;
    const searchJson = JSON.stringify(searchRows);
    runtimeDataHash.update(searchJson);
    writeFileSync(
      path.join(searchIndexDir, `${source}.json`),
      searchJson,
    );
  }

  const bankDataVersion = runtimeDataHash.digest("hex").slice(0, 12);
  writeFileSync(
    path.join(root, "src/lib/generated/bankDataVersion.generated.ts"),
    `// AUTO-GENERATED by scripts/generate-bank-route-index.mjs - do not edit.

export const BANK_DATA_VERSION = "${bankDataVersion}";
`,
  );

  rmSync(path.join(root, "src/lib/generated/bankRouteIndex.generated.ts"), { force: true });

  console.log(
    `generate-bank-route-index: wrote ${totalRouteRows} route rows, ${totalDetailRows} detail rows, and ${totalSearchRows} search rows`,
  );
} finally {
  await server.close();
}
